# arithmetic operators
x = 900
y = 100

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  # modules

# Comparison operators
num1 = 78
num2 = 45
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)  # Equal to
print(num1 != num2)  # Not equal to

#  assignment operators
a = 7
print(a)

b = 13
b += 2
print(b)

c = 14
c -= 2
print(c)

# Logical operators
number1 = 8
number2 = 20
print(number1 > number2 and number2 > number1)
print(number1 > number2 or number2 > number1)
print(not (number1 > number2 or number2 > number1))


